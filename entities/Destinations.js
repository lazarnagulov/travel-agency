class Destinations
{
    constructor(name, description, photos, type, typeOfTransport, price, maxTravelers)
    {
        this.name = name;
        this.description = description;
        this.photos = photos;
        this.type = type;
        this.typeOfTransport = typeOfTransport;
        this.price = price;
        this.maxTravelers = maxTravelers;
    }
}