class TravelAgency
{
    constructor(name, address, yearOfOpening, logo, phoneNumber, email, destinations)
    {
        this.name = name;
        this.address = address;
        this.yearOfOpening = yearOfOpening;
        this.logo = logo;
        this.phoneNumber = phoneNumber;
        this.email = email;
        this.destinations = destinations;
    }

}